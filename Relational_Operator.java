public class Relational_Operator {
    
    public static void main(String[] args) {
        System.out.println("=========Relational Operator=======");

        //Equal to
        boolean resultEqual = (5 == 5);
        System.out.println("Equal to: " + resultEqual);
    
        //Not Equal to
        boolean resultNotEqual = (10 != 5);
        System.out.println("Not Equal to: " + resultNotEqual);

        //Greater than
        boolean resultGreaterThan = (10 > 6);
        System.out.println("Greater Than: " + resultGreaterThan);

        //Less than
        boolean resultLessThan = (7 < 5);
        System.out.println("Less Than: " + resultLessThan);

        //Greater Than or Equal to
        boolean resultGreaterEqual = (10 >= 15);
        System.out.println("Greater than Equal to: " + resultGreaterEqual);

        // Less Than or Equal to
        boolean resultLessEqual = (16<= 5);
        System.out.println("Less than Equal to: " + resultLessEqual);
    }

}
